def get_dams_geojson():
    """Convert dams data to GeoJSON format"""
    features = []
    
    for dam in MAJOR_DAMS:
        feature = {
            "type": "Feature",
            "properties": {
                "id": dam["id"],
                "name": dam["name"],
                "name_en": dam["name_en"],
                "basin_id": dam["basin_id"],
                "river": dam["river"],
                "capacity_mcm": dam["capacity_mcm"],
                "type": dam["type"],
                "year_built": dam["year_built"],
                "height_m": dam["height_m"],
                "province": dam["province"],
                "marker_type": "dam",
            },
            "geometry": {
                "type": "Point",
                "coordinates": [dam["lon"], dam["lat"]],
            },
        }
        features.append(feature)
    
    return {
        "type": "FeatureCollection",
        "features": features,
    }
